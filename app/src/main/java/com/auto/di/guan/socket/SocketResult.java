package com.auto.di.guan.socket;

public class SocketResult {
    private String type;
    private String projectId;
    private String name;
    private String nameCode;

    private String status;
    private String voltage;
    private String electricity;
    private String errorCode;

    private String statusValue;
    private String voltageValue;
    private String electricityValue;
    private String errorCodeValue;


    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getProjectId() {
        return projectId;
    }

    public void setProjectId(String projectId) {
        this.projectId = projectId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNameCode() {
        return nameCode;
    }

    public void setNameCode(String nameCode) {
        this.nameCode = nameCode;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getVoltage() {
        return voltage;
    }

    public void setVoltage(String voltage) {
        this.voltage = voltage;
    }

    public String getElectricity() {
        return electricity;
    }

    public void setElectricity(String electricity) {
        this.electricity = electricity;
    }

    public String getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(String errorCode) {
        this.errorCode = errorCode;
    }

    public String getStatusValue() {
        return statusValue;
    }

    public void setStatusValue(String statusValue) {
        this.statusValue = statusValue;
    }

    public String getVoltageValue() {
        return voltageValue;
    }

    public void setVoltageValue(String voltageValue) {
        this.voltageValue = voltageValue;
    }

    public String getElectricityValue() {
        return electricityValue;
    }

    public void setElectricityValue(String electricityValue) {
        this.electricityValue = electricityValue;
    }

    public String getErrorCodeValue() {
        return errorCodeValue;
    }

    public void setErrorCodeValue(String errorCodeValue) {
        this.errorCodeValue = errorCodeValue;
    }
}
